enum DateFormatString: String {
    case dashyyyyMMddHHmmss = "yyyy-MM-dd HH:mm:ss"
    case dashyyyyMMddHHmm = "yyyy-MM-dd HH:mm"
    case dashyyyyMMdd = "yyyy-MM-dd"
    case dashddMMyyyy = "dd-MM-yyyy"
    case slashyyyyMMddHHmmss = "yyyy/MM/dd HH:mm:ss"
    case slashyyyyMMddHHmm = "yyyy/MM/dd HH:mm"
    case slashyyyyMMdd = "yyyy/MM/dd"
    case slashddMMyyyy = "dd/MM/yyyy"
}
